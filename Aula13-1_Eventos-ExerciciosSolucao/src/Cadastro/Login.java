    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:
        
        String username = jTextFieldUsername.getText();
        String password = jPasswordFieldPassword.getText();
        
        boolean validate = uc.validate(username, password);
        
        if(validate){
            boolean isSuperUser = uc.isSuperUser(username);
            if(isSuperUser){
                Cadastro c = new Cadastro();
                c.setVisible(true);
                c.login(this);
            }else{
                Welcome w = new Welcome();
                w.setVisible(true);
                w.login(this);
            }
            
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "USERNAME OR PASSWORD INCORRECT", "ERROR"
                    , JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButtonLoginActionPerformed

