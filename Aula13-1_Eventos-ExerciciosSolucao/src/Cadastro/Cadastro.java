    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        String username = jTextFieldUsername.getText();
        String completeName, password;
        int selectedItem;

        Usuario user = uc.getUser(username);

        if (user == null) {
            completeName = jTextFieldCompleteName.getText();
            password = jPasswordFieldPassword.getText();
            selectedItem = jComboBoxSuperUser.getSelectedIndex();

            uc.addUser(new Usuario(completeName, username, password, (selectedItem != 0)));
        } else {
            int reply = JOptionPane.showConfirmDialog(null, "DO YOU ARE OVERWRITING AN EXISTED USER?",
                    "WARNING", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                if (reply == JOptionPane.YES_OPTION) {
                    jTextFieldUsername.setText(user.getUserName());

                    completeName = jTextFieldCompleteName.getText();
                    password = jPasswordFieldPassword.getText();
                    selectedItem = jComboBoxSuperUser.getSelectedIndex();

                    
                    uc.changeCompleteName(user.getUserName()
                            , user.getPassword(), completeName);
                    uc.changePassword(user.getUserName(), user.getPassword(), password);
                    user.setPassword(password);
                    uc.changeUsername(user.getUserName(), user.getPassword(), username);
                }

            }
        }
    }//GEN-LAST:event_jButtonRegisterActionPerformed

