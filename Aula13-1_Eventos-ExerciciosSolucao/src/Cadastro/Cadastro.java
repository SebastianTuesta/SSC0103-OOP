    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        String username = jTextFieldUsername.getText();

        Usuario user = uc.getUser(username);

        if (user != null) {
            jTextFieldUsername.setText(user.getUserName());
            jTextFieldCompleteName.setText(user.getCompleteName());
            jComboBoxSuperUser.setSelectedIndex(user.isIsSuperUser() ? 1 : 0);
        } else {
            JOptionPane.showMessageDialog(rootPane, "USERNAME INCORRECT", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed

